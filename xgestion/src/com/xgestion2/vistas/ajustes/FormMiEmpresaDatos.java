    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        JFileChooser se = new JFileChooser();
        javax.swing.filechooser.FileFilter imageFilter = new FileNameExtensionFilter("Imagenes", ImageIO.getReaderFileSuffixes());
        se.setFileFilter(imageFilter);
        se.setFileSelectionMode(JFileChooser.FILES_ONLY);       
        int estado = se.showOpenDialog(null);
        if(estado == JFileChooser.APPROVE_OPTION)
        {
            try {
                int longitudBytes = (int)se.getSelectedFile().length();
                Image icono = ImageIO.read(se.getSelectedFile()).getScaledInstance(jLabelLogo.getWidth(), jLabelLogo.getHeight(), Image.SCALE_DEFAULT);
                jLabelLogo.setIcon(new ImageIcon(icono));
                jLabelLogo.updateUI(); 

                byte[] imageData = new byte[(int) se.getSelectedFile().length()];
                FileInputStream io = new FileInputStream(se.getSelectedFile());
                io.read(imageData);
                io.close();
                x.setLogo(imageData);

            } catch (FileNotFoundException ex) {
                FormPrincipal.logger.error("Error",ex);
            }catch (IOException ex){
                FormPrincipal.logger.error("Error",ex);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void cargarValores(){
        x = FormPrincipal.miEmpresa;
        jTextField1.setText(x.getId().toString());
        jTextField2.setText(x.getNombre());
        jTextField3.setText(x.getDireccion());
        jTextField4.setText(x.getCuit());
        jTextField5.setText(x.getTelefono());
        
        jLabelLogo.setIcon(new ImageIcon(scaleImage(x.getLogo(),jLabelLogo.getWidth(),jLabelLogo.getHeight())));
        repaint();
        revalidate();
    }
    
    public Image scaleImage(byte[] fileData, int width, int height) {
    	ByteArrayInputStream in = new ByteArrayInputStream(fileData);
        Image scaledImage = null;
    	try {
            BufferedImage img = ImageIO.read(in);
            if(height == 0) {
                height = (width * img.getHeight())/ img.getWidth(); 
            }
            if(width == 0) {
    		width = (height * img.getWidth())/ img.getHeight();
            }
            scaledImage = img.getScaledInstance(width, height, Image.SCALE_SMOOTH);

    	} catch (IOException e) {
            FormPrincipal.logger.error("ERROR: ",e);
    	}
        return scaledImage;
    }

